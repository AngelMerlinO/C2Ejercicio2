    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        switch (i) {
             case 1:
                tipoEmpleado = new C2Ejercicio2(entrada1.getText());
                texto1.setText(C2Ejercicio2.leerdatos("Directivo"));
                break;
            case 2:
                tipoEmpleado = new Directivo(entrada1.getText());
                texto1.setText(C2Ejercicio2.leerdatos("Operario"));
                break;
            case 3:
                tipoEmpleado = new Operario(entrada1.getText());
                texto1.setText(C2Ejercicio2.leerdatos("Oficial"));
                break;
            case 4:
                tipoEmpleado = new Oficial(entrada1.getText());
                texto1.setText(C2Ejercicio2.leerdatos("TÃ©cnico"));
                break;
            case 5:
                tipoEmpleado = new Tecnico(entrada1.getText());
                texto1.setText("Has Terminado");
                jButton1.setEnabled(false);
                entrada1.setEditable(false);
                break;    
            default:
                texto1.setText(C2Ejercicio2.leerdatos("Has Terminado"));
                jButton1.setEnabled(false);
                break;
        }
        
        fullText += tipoEmpleado + "\n";
        jTextArea1.setText(fullText);
        entrada1.setText("");
        i++;
    }//GEN-LAST:event_jButton1ActionPerformed

